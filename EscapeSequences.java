package com.aayush;

public class EscapeSequences {

	public static void main(String[] args) {
		
		/*
		 	\t : tab
		 	\b : backspace
		 	\n : new line
		 	\r : carriage return
		 	\f : form feed
		 	\' : single quote
		 	\" : double quote
		 	\\ : backslash
		*/
		
		System.out.println("My favourite book is \"The one thing\" by Gary Keller");
		System.out.println(("Path : D:\\Java\\sources"));
		System.out.println("Text line 1\nText line 2");
		System.out.println("Unicode : \u00A9");
	}

}
